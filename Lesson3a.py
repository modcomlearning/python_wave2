
# Control Structures
# 1. Decision Making - Conditional statements - if, if else, else if
# 2. Looping    - Repeating   - while, for loops

# ================Conditional Statements===============
marks = float(input('Enter Your Marks: '))

if marks < 0:
    print('Invalid Marks')  # true

else:
    print('Valid Marks')    # false


# if else works only when there is one condition  i.e marks < 0